    private void updateSongFormInfo() {
        if (Objects.isNull(song)) {
            return;
        }

        txtID.setText(String.valueOf(song.getSongID()));
        txtTitle.setText(song.getTitle());
        txtLength.setText(String.valueOf(song.getLength()));
        cmbBand.setSelectedItem(song.getBand());
    }

    private void updateSongInfo() throws Exception {
        if (Objects.nonNull(song) && Objects.nonNull(song.getSongID())) {
            song.setSongID(Long.valueOf(txtID.getText()));
        }
        song.setTitle(txtTitle.getText());
        song.setLength(Integer.valueOf(txtLength.getText()));
        song.setBand((Band) cmbBand.getSelectedItem());
    }

    private void fillBandCombobox() {
        try {
            Response response = Controller.getInstance().getAllBands();

            if (response.getException() == null) {
                List<Band> bands = (List<Band>) response.getResult();
                if (bands.isEmpty()) {
                    JOptionPane.showMessageDialog(this, "System could not find bands", "Error", JOptionPane.ERROR_MESSAGE);
                } else {
                    JOptionPane.showMessageDialog(this, "System found bands", "Found bands", JOptionPane.INFORMATION_MESSAGE);
                }
                cmbBand.setModel(new DefaultComboBoxModel<>(bands.toArray()));
            } else {
                JOptionPane.showMessageDialog(this, "System could not find bands\n" + response.getException().getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "System could not find bands\n" + ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }
