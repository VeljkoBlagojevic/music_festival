    public void fillTableStages() {
        try {
            Response response = Controller.getInstance().getAllStages();
            if (response.getException() == null) {
                List<Stage> stages = (List<Stage>) response.getResult();
                if (stages.isEmpty()) {
                    JOptionPane.showMessageDialog(this, "System could not find stages\n", "Error", JOptionPane.ERROR_MESSAGE);
                } else {
                    JOptionPane.showMessageDialog(this, "System has found stages\n", "Found stages", JOptionPane.INFORMATION_MESSAGE);
                }
                model = new StageTableModel(stages);
                tblStages.setModel(model);
            } else {
                JOptionPane.showMessageDialog(this, "System could not find stages\n" + response.getException().getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "System could not find stages\n" + ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }
