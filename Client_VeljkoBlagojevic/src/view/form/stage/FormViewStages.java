        String filteringCriteria = txtFilter.getText();

        Stage searchStageParam = new Stage();
        searchStageParam.setName(filteringCriteria);

        try {
            Response response = Controller.getInstance().getAllStagesFiltered(searchStageParam);

            if (response.getException() == null) {
                List<Stage> stages = (List<Stage>) response.getResult();
                if (stages.isEmpty()) {
                    JOptionPane.showMessageDialog(this, "System could not find stages with search criteria\n", "Error", JOptionPane.ERROR_MESSAGE);
                } else {
                    JOptionPane.showMessageDialog(this, "System has found stages with search criteria\n", "Found stages", JOptionPane.INFORMATION_MESSAGE);
                }
                model = new StageTableModel(stages);
                tblStages.setModel(model);
            } else {
                JOptionPane.showMessageDialog(this, "System could not find stages with search criteria\n" + response.getException().getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "System could not find stages with search criteria\n" + ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }

