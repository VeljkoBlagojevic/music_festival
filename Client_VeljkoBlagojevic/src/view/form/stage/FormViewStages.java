    private void btnDetailsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDetailsActionPerformed
        final int selectedItemsCount = tblStages.getSelectionModel().getSelectedItemsCount();
        if (selectedItemsCount == 0) {
            JOptionPane.showMessageDialog(this, "You have not selected stage you want to see details for!", "Invalid selection", JOptionPane.ERROR_MESSAGE);
            return;
        }
        if (selectedItemsCount > 1) {
            JOptionPane.showMessageDialog(this, "You have selected multiple stages. Please select only one stage you want to see details for!", "Invalid selection", JOptionPane.ERROR_MESSAGE);
            return;
        }
        int selectedRow = tblStages.getSelectionModel().getSelectedIndices()[0];
        Stage stage = ((StageTableModel) tblStages.getModel()).getStageAt(selectedRow);
        new FormStage(this, false, stage).setVisible(true);
        this.dispose();
    }//GEN-LAST:event_btnDetailsActionPerformed

