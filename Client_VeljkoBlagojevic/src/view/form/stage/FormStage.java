    private void updateStageFormInfo() throws Exception {
        if (Objects.isNull(stage)) {
            return;
        }

        txtID.setText(String.valueOf(stage.getStageID()));
        txtName.setText(stage.getName());
        txtLocation.setText(String.valueOf(stage.getLocation()));
        txtAudienceCapacity.setText(stage.getAudienceCapacity() + " people");
        txtSurfaceArea.setText(stage.getSurfaceArea() + " m2");
    }

    private void updateStageInfo() throws Exception {
        if (Objects.nonNull(stage) && Objects.nonNull(stage.getStageID())) {
            stage.setStageID(Long.valueOf(txtID.getText()));
        }
        stage.setName(txtName.getText());
        stage.setLocation(txtLocation.getText());
        stage.setAudienceCapacity(Integer.valueOf(txtAudienceCapacity.getText().split(" ")[0]));
        stage.setSurfaceArea(Integer.valueOf(txtSurfaceArea.getText().split(" ")[0]));
    }
