    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        try {
            String username = txtUsername.getText().trim();
            String password = String.copyValueOf(txtPassword.getPassword());
            validateForm(username, password);
            User u = new User();
            u.setUsername(username);
            u.setPassword(password);
            Request request = new Request(u, Operation.LOGIN);
            Communication.getInstance().getSender().send(request);
            Response response = (Response) Communication.getInstance().getReceiver().receive();
            if (response.getException() == null) {
                User loggedInUser = (User) response.getResult();
                JOptionPane.showMessageDialog(this, "Welcome, " + loggedInUser.getFirstname());
                this.dispose();
                new FormMain(loggedInUser).setVisible(true);
            } else {
                JOptionPane.showMessageDialog(this, "System could not login\n" + response.getException().getMessage(), "Login Error", JOptionPane.ERROR_MESSAGE);
            }
        } catch (Exception ex) {
            ex.printStackTrace();
            JOptionPane.showMessageDialog(this, "System could not login\n" + ex.getMessage(), "Login Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnLoginActionPerformed

