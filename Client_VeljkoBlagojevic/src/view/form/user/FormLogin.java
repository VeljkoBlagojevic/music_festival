    private void validateForm(String username, String password) throws Exception {
        String error = "";
        lblUsernameError.setText("");
        lblPasswordError.setText("");
        if (username == null || username.isEmpty()) {
            lblUsernameError.setText("Username cannot be empty!");
            error += "Username cannot be empty!\n";
        }
        if (password == null || password.length() < 5) {
            lblPasswordError.setText("Password must contain at least five chars!");
            error += "Password must contain at least five chars!\n";
        }
        if (!error.isEmpty()) {
            throw new Exception(error);
        }
    }

